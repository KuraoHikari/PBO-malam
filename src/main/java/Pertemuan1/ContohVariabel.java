public class ContohVariabel {
    public static void main(String[] args) {
        int nilai = 12;
        char karakter = 'A';
        double pecahan = 123.45;
        float pecah = 3.14f;
        String message = "Hello";
        
        System.out.println("Variable int: " + nilai);
        System.out.println("Variable char: " + karakter);
        System.out.println("Variable double: " + pecahan);
        System.out.println("Variable float: " + pecah);
        System.out.println("Variable String: " + message);
    }
}
