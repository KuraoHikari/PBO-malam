public class Aritmatika {
    public static void main(String[] args) {
        int x;
        int y;
        int z;

        x = 15;
        y = 4;

        z = x + y;
        System.out.println("Hasil penjumlahan x + y = " + z);

        z = x - y;
        System.out.println("Hasil pengurangan x - y = " + z);

        z = x / y;
        System.out.println("Hasil pembagian x / y = " + z);

        z = x * y;
        System.out.println("Hasil perkalian x * y = " + z);
    }
}
